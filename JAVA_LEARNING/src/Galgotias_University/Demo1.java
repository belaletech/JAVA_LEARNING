import java.util.*;

public class Demo1 {
	public static void main(String[] args)
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter the number for storing in array");
		int n=sc.nextInt();
		
		int arr[]=new int[n];
		for(int i=0;i<n;i++)
		{
			System.out.println("Element of array"+arr[n]);
			n++;
		}
		
	}

}
